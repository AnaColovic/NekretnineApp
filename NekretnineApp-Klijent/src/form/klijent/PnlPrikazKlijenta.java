    @Override
    public void popuniPolja(OpstaDomenskaKlasa odk) {
        klijent.popuniPolja(odk);
        UgovorOKupoprodajiNekretnine ugovor = new UgovorOKupoprodajiNekretnine();
        UgovorOZakupuNekretnine ugovor1 = new UgovorOZakupuNekretnine();
        try {
            ugovor.setKlijent((Klijent) klijent.preuzmiPolja());
            ugovor1.setKlijent((Klijent) klijent.preuzmiPolja());
            List<UgovorOKupoprodajiNekretnine> prodajaUg = Kontroler.getInstance().pretraziUgovoreOKupoprodaji(ugovor);
            List<UgovorOZakupuNekretnine> zakupUg = Kontroler.getInstance().pretraziUgovoreOZakupu(ugovor1);
            TableModelUgovoriOIzdavanju modelI = (TableModelUgovoriOIzdavanju) tblIzdavanje.getModel();
            TableModelUgovoriOKupoprodaji modelP = (TableModelUgovoriOKupoprodaji) tblProdaja.getModel();
            modelI.setUgovori(zakupUg);
            modelP.setUgovori(prodajaUg);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

