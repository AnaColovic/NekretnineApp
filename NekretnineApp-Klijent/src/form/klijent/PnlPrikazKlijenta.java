    @Override
    public void dekorisiPanel() {
        Box boxes[] = new Box[1];
        boxes[0] = Box.createHorizontalBox();

        boxes[0].createGlue();

        pnlKlijent.add(boxes[0]);

        klijent = new PnlUnosKlijenta(PanelModeEnum.PRIKAZ);
        klijent.postaviVidljivostPoljima(false);

        klijent.setPreferredSize(new Dimension(850, 190));

        boxes[0].add(klijent);

        TableModelUgovoriOIzdavanju izdavanje = new TableModelUgovoriOIzdavanju(new LinkedList<>());
        tblIzdavanje.setModel(izdavanje);
        TableModelUgovoriOKupoprodaji prodaja = new TableModelUgovoriOKupoprodaji(new LinkedList<>());
        tblProdaja.setModel(prodaja);

    }

