    private void btnPrikaziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikaziActionPerformed
        int row = tblProdaja.getSelectedRow();
        TableModelUgovoriOKupoprodaji model = (TableModelUgovoriOKupoprodaji) tblProdaja.getModel();
        int row1 = tblIzdavanje.getSelectedRow();
        TableModelUgovoriOIzdavanju model1 = (TableModelUgovoriOIzdavanju) tblIzdavanje.getModel();
        if (row != -1) {
            UgovorOKupoprodajiNekretnine ugovor = model.getUgovori().get(row);
            PnlNoviUgovorOProdajiNekretnine panel = new PnlNoviUgovorOProdajiNekretnine(PanelModeEnum.PRIKAZ);
            panel.popuniPolja(ugovor);

            FrmDialog forma = new FrmDialog((FrmForma) SwingUtilities.getWindowAncestor(this), true, panel);
            forma.setVisible(true);
        } else {

            if (row1 != -1) {
                UgovorOZakupuNekretnine ugovor = model1.getUgovori().get(row);
                PnlNoviUgovorOIzdavanjuNekretnine panel = new PnlNoviUgovorOIzdavanjuNekretnine(PanelModeEnum.PRIKAZ);
                panel.popuniPolja(ugovor);
                panel.postaviVidljivostPoljima(false);
                FrmDialog form = new FrmDialog((Frame) SwingUtilities.getWindowAncestor(this), true, panel);

                form.setVisible(true);
            }
        }

        if (row == -1 && row1 == -1) {
            JOptionPane.showMessageDialog(this, "Morate izabrati ugovor");
        }
    }//GEN-LAST:event_btnPrikaziActionPerformed

