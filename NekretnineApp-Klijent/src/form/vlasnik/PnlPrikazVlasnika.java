    @Override
    public void popuniPolja(OpstaDomenskaKlasa odk) {
        vlasnik.popuniPolja(odk);
        Nekretnina nekProdaja = new NekretninaZaProdaju();
        Nekretnina nekIzdavanje = new NekretninaZaIzdavanje();
        try {
            nekProdaja.setVlasnik((Vlasnik) vlasnik.preuzmiPolja());
            nekIzdavanje.setVlasnik((Vlasnik) vlasnik.preuzmiPolja());
            List<Nekretnina> prodaja = Kontroler.getInstance().pretraziNekretnine(nekProdaja);
            List<Nekretnina> zakup = Kontroler.getInstance().pretraziNekretnine(nekIzdavanje);
            TableModelNekretnina modelI = (TableModelNekretnina) tblIznajmljivanje.getModel();
            TableModelNekretnina modelP = (TableModelNekretnina) tblProdaja.getModel();
            modelI.setNekretnine(zakup);
            modelP.setNekretnine(prodaja);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }
    
