    private void btnPrikaziZaIznajmljivanjeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikaziZaIznajmljivanjeActionPerformed
        TableModelNekretnina model = (TableModelNekretnina) tblIznajmljivanje.getModel();
        int row = tblIznajmljivanje.getSelectedRow();
        if (row != -1) {
            Nekretnina nekretnina = model.getNekretnine().get(row);
            PnlNovaNekretnina panel = new PnlNovaNekretnina(PanelModeEnum.PRIKAZ, true);
            panel.popuniPolja(nekretnina);
            FrmDialog form = new FrmDialog((Frame) SwingUtilities.getWindowAncestor(this), true,panel);
            form.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Morate izabrati nekretninu", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPrikaziZaIznajmljivanjeActionPerformed

