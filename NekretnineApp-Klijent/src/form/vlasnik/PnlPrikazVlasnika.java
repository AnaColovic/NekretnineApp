    @Override
    public void dekorisiPanel() {
        Box boxes[] = new Box[1];
        boxes[0] = Box.createHorizontalBox();
        
        boxes[0].createGlue();
        
        pnlVlasnik.add(boxes[0]);
        
        vlasnik = new PnlUnosVlasnika(PanelModeEnum.PRIKAZ);
        vlasnik.postaviVidljivostPoljima(false);
        
        vlasnik.setPreferredSize(new Dimension(400, 220));
        
        boxes[0].add(vlasnik);
        
        TableModelNekretnina izdavanje = new TableModelNekretnina(new LinkedList<>(), NamenaNekretnine.ZA_IZDAVANJE);
        tblIznajmljivanje.setModel(izdavanje);
        TableModelNekretnina prodaja = new TableModelNekretnina(new LinkedList<>(), NamenaNekretnine.ZA_PRODAJU);
        tblProdaja.setModel(prodaja);
    }
    
